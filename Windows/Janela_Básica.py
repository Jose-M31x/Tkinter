# Importação da biblioteca Tkinter
# Para intalar, basta usar o comando "pip install tk" no terminal
from tkinter import *


def main():
    # Criação da instância da janela usando a classe "TK"
    application = Tk()

    # Criação do loop principal
    # "mainloop" é o método que coloca todo o conteúdo em exibição na janela
    application.mainloop()


if __name__ == '__main__':
    main()
